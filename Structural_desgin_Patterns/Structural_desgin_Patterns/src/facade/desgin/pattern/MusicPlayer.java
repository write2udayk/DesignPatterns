package facade.desgin.pattern;

/**
 * The {@code MusicPlayer} class is responsible for controlling the playback of
 * music. It provides methods to play, pause, and stop music.
 */
public class MusicPlayer {

    /**
     * Plays the music. This method simulates starting the music playback.
     */
    public void playMusic() {
        System.out.println("Playing music!!");
    }

    /**
     * Pauses the music. This method simulates pausing the music playback.
     */
    public void pauseMusic() {
        System.out.println("Music paused!!");
    }

    /**
     * Stops the music. This method simulates stopping the music playback.
     */
    public void stopMusic() {
        System.out.println("Stopped music!");
    }
}
